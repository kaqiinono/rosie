function makeSupp(n: number, text: string, factorN: number, inner: number, ans: number, isSubtract = false): Problem {
  const op = isSubtract ? '−' : '+'
  const coeff = ans / factorN
  const label = coeff === 100 ? '100' : coeff === 1000 ? '1000' : `${coeff}`
  return {
    id: `34-S${n}`,
    title: `补充题${n}`,
    tag: 'type2',
    tagLabel: '分配律',
    text,
    analysis: [
      `提取公因数${factorN}`,
      `= ${factorN}×(${inner})`,
      `= ${factorN}×${label}`,
      `= ${ans}`,
    ],
    type: 'none',
    finalQ: '计算结果是多少？',
    finalUnit: '',
    finalAns: ans,
  }
}

